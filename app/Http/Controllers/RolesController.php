    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index(Request $request)
    {
        if($request->ajax()) {

            $roles = [];
            if (auth()->user()->hasRole("owner")) {
                $roles = Role::where("barangay_id", auth()->user()->{User::FIELD_BARANGAY_ID})->get();
            
            } else {
                $roles = Role::get();

            }
            $defaultRoles = Role::whereNull("barangay_id")->orWhere("barangay_id", 0)->whereIn("name", ["owner", "tanod"])->get();
            return ["data"=> $roles->toArray() + $defaultRoles->toArray()];
        }
        return view("roles.index");
    }

